function isVideoExt(fn){ return /\.(mp4|webm|mov)$/i.test(fn); }

/* Precomputed once — every consumer (D2C + B2B) reads from this. */
const PHOTOS = RAW.map((r, i) => ({
  id: i,
  title: r[0],
  kind: r[1],
  file: r[2],
  src: BASE + encodeURIComponent(r[2]),
  video: isVideoExt(r[2])
}));

/* Grouped index for fast category pulls — this is what the D2C
   site leans on to populate the Hero, Category Strip, The Edit,
   and Bestsellers straight from real production frames. */
const PHOTOS_BY_KIND = PHOTOS.reduce((acc, p) => {
  (acc[p.kind] = acc[p.kind] || []).push(p);
  return acc;
}, {});

/* Random, non-repeating pull of `count` still frames from a
   category. Used by both portals wherever a "randomized" or
   "most relevant" image set is needed. */
function pickFromKind(kind, count, opts){
  opts = opts || {};
  const pool = (PHOTOS_BY_KIND[kind] || []).filter(p => opts.allowVideo ? true : !p.video);
  if (!pool.length) return [];
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

