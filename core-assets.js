function buildProductData(p){
  const spec = GARMENT_SPECS[p.kind];
  if (!spec) return null;

  const idNum = String(p.id % 999 + 1).padStart(3,'0');
  const generatedCode = `RX-${spec.code}-${idNum}`;
  const desc = DESC_FRAGMENTS[p.id % DESC_FRAGMENTS.length];
  const moqVariance = (p.id % 3) * 50;

  const base = {
    code: generatedCode,
    name: `${generatedCode}: ${spec.label}`,
    desc,
    material: spec.material,
    moq: spec.moq + moqVariance,
    sizes: spec.sizes.join(' / '),
    price: null,
    colorways: null
  };

  const override = PRODUCT_OVERRIDES[p.file];
  return override ? { ...base, ...override } : base;
}
