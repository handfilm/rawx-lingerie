/* ---------------- 2. Extension helpers ---------------- */
function getExt(filename){
  var m = /\.([a-zA-Z0-9]+)$/.exec(filename);
  return m ? m[1].toLowerCase() : '';
}

function isVideoExt(filename){
  var ext = getExt(filename);
  return ext === 'mp4' || ext === 'mov' || ext === 'webm';
}

function isImageExt(filename){
  var ext = getExt(filename);
  return ext === 'jpg' || ext === 'jpeg' || ext === 'png' || ext === 'webp' || ext === 'gif' || ext === 'avif';
}

/* ---------------- 3. Kind classification ----------------
   Derives a product "kind" tag from the filename prefix so the
   gallery can filter/group without a separate lookup table on
   disk. Add new prefixes here as new product lines are added. */
var KIND_MAP = [
  { test: /^2set/i,          kind: 'twoset' },
  { test: /^activewear/i,    kind: 'activewear' },
  { test: /^bodysuit/i,      kind: 'bodysuit' },
  { test: /^essential/i,     kind: 'essential' },
  { test: /^thong/i,         kind: 'thong' },
  { test: /^panty/i,         kind: 'panty' },
  { test: /^lingerie/i,      kind: 'lingerie' },
  { test: /^hunkemoller/i,   kind: 'lingerie' }
];

function kindFromFilename(filename){
  for (var i = 0; i < KIND_MAP.length; i++){
    if (KIND_MAP[i].test.test(filename)) return KIND_MAP[i].kind;
  }
  return 'misc';
}

/* ---------------- 4. PHOTOS registry ----------------
   One entry per file, with the correct relative path based on
   whether it's a video or image asset. */
const PHOTOS = RAW.map(function(filename){
  var video = isVideoExt(filename);
  var folder = video ? 'videos' : 'images';
  return {
    filename: filename,
    src: 'assets/' + folder + '/' + filename,
    kind: kindFromFilename(filename),
    isVideo: video
  };
});

/* ---------------- 5. Query helpers ---------------- */

/* Returns up to `count` PHOTOS entries matching a given kind.
   Pass no count (or 0) to get every match. */
function pickFromKind(kind, count){
  var matches = PHOTOS.filter(function(p){ return p.kind === kind; });
  if (!count) return matches;
  return matches.slice(0, count);
}

/* Returns a random PHOTOS entry matching a given kind, or null
   if the kind has no assets. */
function randomFromKind(kind){
  var matches = PHOTOS.filter(function(p){ return p.kind === kind; });
  if (!matches.length) return null;
  return matches[Math.floor(Math.random() * matches.length)];
}

/* Returns every distinct kind present in the registry. */
function allKinds(){
  var seen = {};
  PHOTOS.forEach(function(p){ seen[p.kind] = true; });
  return Object.keys(seen);
}
